#include <stdio.h>

int main() {
    
    int panjang = 10; 
    int lebar = 7;  
    int luas;

    // Cara 
    luas = panjang * lebar;

    // Hasil
    printf("Panjang: %d cm\n", panjang);
    printf("Lebar: %d cm\n", lebar);
    printf("Luas Persegi Panjang: %d cm\n", luas);

    return 0;
}

